fn gen_matrix(input: &str) -> (usize, Vec<Vec<bool>>) {
    let rows = input.split("\n");
    let mut matrix = vec![];
    let mut row_len = 0;

    for row in rows {
        let mut items = vec![];

        for item in row.chars() {
            items.push(item == '@');
        }

        if items.len() > row_len {
            row_len = items.len();
        }

        matrix.push(items);
    }

    (row_len, matrix)
}

fn is_item(matrix: &Vec<Vec<bool>>, i: usize, j: usize) -> u32 {
    if let Some(row) = matrix.get(i) {
        if let Some(item) = row.get(j) {
            if *item {
                return 1;
            }
        }
    }

    0
}

fn count_adjacent(matrix: &Vec<Vec<bool>>, i: usize, j: usize) -> u32 {
    let mut count = 0;

    // Top Left
    match (i.checked_sub(1), j.checked_sub(1)) {
        (Some(i), Some(j)) => count += is_item(matrix, i, j),
        _ => {}
    }

    // Top
    match (i.checked_sub(1), j) {
        (Some(i), j) => count += is_item(matrix, i, j),
        _ => {}
    }

    // Top right
    match (i.checked_sub(1), j + 1) {
        (Some(i), j) => count += is_item(matrix, i, j),
        _ => {}
    }

    // Center Left
    match (i, j.checked_sub(1)) {
        (i, Some(j)) => count += is_item(matrix, i, j),
        _ => {}
    }

    // Center Center
    //count += is_item(matrix, i, j);

    // Center Right
    count += is_item(matrix, i, j + 1);

    // Bottom Left
    match (i + 1, j.checked_sub(1)) {
        (i, Some(j)) => count += is_item(matrix, i, j),
        _ => {}
    }

    // Bottom Center
    count += is_item(matrix, i + 1, j);

    // Bottom Right
    count += is_item(matrix, i + 1, j + 1);

    //println!("{i}:{j} => {count}");

    count
}

fn print_matrix(matrix: &Vec<Vec<bool>>) {
    for row in matrix {
        for col in row {
            if *col {
                print!("@");
            } else {
                print!(".")
            }
        }
        println!();
    }
}

pub fn run() {
    let (row_len, matrix) = gen_matrix(INPUT);
    let mut count = 0;

    print_matrix(&matrix);
    println!();

    for row in 0..matrix.len() {
        for col in 0..row_len {
            if count_adjacent(&matrix, row, col) < 4 && is_item(&matrix, row, col) == 1 {
                count += 1;
                print!("x");
            } else {
                print!(".");
            }
        }
        println!();
    }
    println!();

    println!("Accessible {count}");
}
